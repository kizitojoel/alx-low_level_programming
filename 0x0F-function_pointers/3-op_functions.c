/**
 * op_add - adds
 * @a: integer a
 * @b: integer b
 *
 * Return: the answer
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract
 * @a: integer a
 * @b: integer b
 *
 * Return: the answer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: integer a
 * @b: integer b
 *
 * Return: the answer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a: integer a
 * @b: integer b
 *
 * Return: the answer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return modulus
 * @a: integer a
 * @b: integer b
 *
 * Return: the answer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
